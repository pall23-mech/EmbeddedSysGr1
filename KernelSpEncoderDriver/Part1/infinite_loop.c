#include <stdio.h>

// A infinite loop to fully load the CPU

int main() {
    // Declare two large floating-point numbers
    double num1 = 12345678.12345678;
    double num2 = 87654321.87654321;
    double result;

    // Infinite loop
    while (1) {
        // Multiply the two numbers
        result = num1 * num2;

    }

    return 0;
}